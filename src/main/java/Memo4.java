public class Memo4 {
    public static void main(String[] args) {

        int a = 10;

        a++;
        System.out.printf("%d\n",a);
        System.out.println(a);

        a--;
        System.out.printf("%d\n",a);
        System.out.println(a);

        a += 5;
        System.out.printf("a += 5 ==> %d\n",a);
        System.out.println("a += 5 ==> "+a);

    }
}
